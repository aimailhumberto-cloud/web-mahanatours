// Helper to get tours by category
export function getToursByCategory(categoryId: CategoryId): Tour[] {
  return ALL_TOURS.filter((t) => t.category === categoryId);
}

// Helper to get a single tour by ID
export function getTourById(id: string): Tour | undefined {
  return ALL_TOURS.find((t) => t.id === id);
}

// Helper to get category by ID
export function getCategoryById(id: CategoryId): Category | undefined {
  return CATEGORIES.find((c) => c.id === id);
}

// Difficulty labels
export function getDifficultyLabel(level: number): string {
  const labels = ["", "Fácil", "Moderado", "Desafiante", "Difícil", "Extremo"];
  return labels[level] || "";
}

// Price type labels
export function getPriceTypeLabel(type: string): string {
  switch (type) {
    case "per-person": return "por persona";
    case "per-group": return "por grupo";
    case "per-vehicle": return "por vehículo";
    default: return "";
  }
}
